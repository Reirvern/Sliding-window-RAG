class CLIInterface:
    def run(self):
        """
        Основной метод запуска CLI интерфейса
        Собирает данные от пользователя и возвращает результат
        
        :return: Результат работы системы (пока заглушка)
        """
        # Шаг 1: Запрос данных у пользователя
        file_path = input("Введите путь к файлу: ")
        question = input("Введите ваш вопрос: ")
        
        # Шаг 2: Здесь будет вызов RAG Engine (пока заглушка)
        print("\n[DEBUG] Передаем данные в RAG Engine...")
        print(f"Файл: {file_path}")
        print(f"Вопрос: {question}")
        
        # Шаг 3: Возвращаем заглушечный результат
        return f"Ответ на вопрос '{question}' будет здесь"